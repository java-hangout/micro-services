package com.javahangout.retail.model;

public class CreateNewValueChainRes {

	private String name;
	private String client;
	private String mineLocation;
	private String miningMethod;
	private String commodity;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getClient() {
		return client;
	}

	public void setClient(String client) {
		this.client = client;
	}

	public String getMineLocation() {
		return mineLocation;
	}

	public void setMineLocation(String mineLocation) {
		this.mineLocation = mineLocation;
	}

	public String getMiningMethod() {
		return miningMethod;
	}

	public void setMiningMethod(String miningMethod) {
		this.miningMethod = miningMethod;
	}

	public String getCommodity() {
		return commodity;
	}

	public void setCommodity(String commodity) {
		this.commodity = commodity;
	}


}
